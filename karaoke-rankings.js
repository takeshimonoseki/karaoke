  const AGE_YEAR_CEILING = {
    teens: 2026,
    twenties: 2019,
    thirties: 2009,
    forties: 1999,
    fifties: 1994,
    sixties: 1989
  };

  function rowFitsAge(row, ageId) {
    const year = Number(row.year);
    if (!Number.isFinite(year)) return true;
    const ceiling = AGE_YEAR_CEILING[ageId];
    return !ceiling || year <= ceiling;
  }

  window.getCuratedKaraokeRanking = function getCuratedKaraokeRanking(ageId, genreId) {
    if (ageId === "all") {
      const scoreMap = new Map();
      const genreSources = genreId === "total"
        ? ["total", "jpop", "rock", "pop", "anime", "ballad"]
        : [genreId, "total"];
      Object.keys(CURATED).forEach((ageKey) => {
        const age = CURATED[ageKey];
        const seen = new Set();
        genreSources.forEach((key) => {
          (age[key] || []).forEach((row, index) => {
            const k = `${row.title}|${row.artist}`;
            if (seen.has(k)) return;
            seen.add(k);
            const rowScore = Math.max(1, 100 - index);
            const existing = scoreMap.get(k);
            if (existing) {
              existing.score += rowScore;
            } else {
              scoreMap.set(k, { row: { ...row, ageGroups: [] }, score: rowScore });
            }
          });
        });
      });
      return [...scoreMap.values()]
        .sort((a, b) => b.score - a.score)
        .map((entry) => entry.row);
    }

    const age = CURATED[ageId] || CURATED.forties;
    const ageGroup = CURATED_AGE_MAP[ageId];
    const mapRow = (row) => ({
      ...row,
      ageGroups: row.ageGroups?.length ? row.ageGroups : (ageGroup ? [ageGroup] : [])
    });

    if (genreId === "total") {
      const merged = [];
      const seen = new Set();
      ["total", "jpop", "rock", "pop", "anime", "ballad"].forEach((key) => {
        (age[key] || []).forEach((row) => {
          if (!rowFitsAge(row, ageId)) return;
          const songKey = `${row.title}|${row.artist}`;
          if (seen.has(songKey)) return;
          seen.add(songKey);
          merged.push(row);
        });
      });
      return merged.map(mapRow);
    }

    const rows = age[genreId] || age.total || [];
    return rows.map(mapRow);
  };
